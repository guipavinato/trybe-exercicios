const balance = 100;

function sumBalance(value) { return balance + value }

function subBalance(value) { return balance - value }

function multBalance(value) { return balance * value }

function divBalance(value) { return balance / value }

console.log(sumBalance(10));
console.log(subBalance(10));
console.log(multBalance(10));
console.log(divBalance(10));
